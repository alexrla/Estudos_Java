public class App {
    public static void main(String[] args) {
        // Principais tipos de dados
        int integerNumber = 2;
        double doubleValue = 5.2;
        boolean option = true;
        char letter = 65; // A
        String phrase = "Linguagem Java";

        System.out.println(integerNumber); // 2
        System.out.println(doubleValue); // 5.2
        System.out.println(option); // true
        System.out.println(letter); // A
        System.out.println(phrase); // Linguagem Java
    }
}
