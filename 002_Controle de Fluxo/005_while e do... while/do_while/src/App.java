// Exibindo a sequência numérica de 1 a 5

public class App {
    public static void main(String[] args) {
        // Inicialização
        int i = 0;

        do {

            System.out.println(i + 1);

            // Atualização
            i++; 
            
        } while(i < 5); // Condição
    }
}
